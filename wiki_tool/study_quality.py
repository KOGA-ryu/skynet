def is_bad_title(title: str) -> bool:
    cleaned = title.strip()
    if not cleaned:
        return True
    if BAD_TITLE_RE.search(cleaned):
        return True
    return cleaned == cleaned.lower()


def chapter_label_needs_attention(chapter_title: str, *, chapter_number: int | None) -> bool:
    cleaned = chapter_title.strip()
    if not cleaned:
        return True
    normalized = normalize_chapter_title(cleaned, chapter_number=chapter_number)
    if cleaned != normalized:
        return True
    if re.search(r"\s\d{1,4}$", cleaned):
        return True
    if "|" in cleaned:
        return True
    return False


def row_looks_like_page_junk(title_path: str, reader_text: str) -> bool:
    compact = clean_source_text(f"{title_path}\n{reader_text}")
    clean_title = clean_source_text(title_path)
    clean_reader = clean_source_text(reader_text)
    if JUNK_RE.search(compact):
        return True
    if ROW_TITLE_JUNK_RE.search(clean_title):
        return True
    if ROW_TEXT_JUNK_RE.search(clean_reader):
        return True
    if is_junk_section(title_path, reader_text):
        return True
    lines = [line.strip() for line in reader_text.splitlines() if line.strip()]
    if len(lines) >= 3 and sum(1 for line in lines[:20] if re.match(r"^[A-Z][A-Z ,.'’\\*&]+[—-]", line.strip("`* "))) >= 3:
        return True
    if looks_like_contents_dump(clean_reader, lines):
        return True
    return False


def looks_like_contents_dump(clean_reader: str, lines: list[str]) -> bool:
    if not lines:
        return False
    has_code_fence = clean_reader.startswith("```")
    stripped = clean_reader.strip()
    table_like_lines = [
        line
        for line in lines[:10]
        if line.startswith("|")
        or line.endswith("|")
        or re.fullmatch(r"[|:\- ]{5,}", line) is not None
    ]
    if not has_code_fence:
        if len(lines) >= 3:
            if len(table_like_lines) < 3:
                return False
        elif not (
            stripped.startswith("|")
            and stripped.endswith("|")
            and stripped.count("|") >= 8
        ):
            return False
    sample = " ".join(line.strip("`*| ") for line in lines[:10])
    hint_hits = len(CONTENTS_HINT_RE.findall(sample))
    number_hits = len(PAGE_NUMBER_CLUSTER_RE.findall(sample))
    section_hits = len(re.findall(r"\b\d+\.\d+\b", sample))
    if len(lines) == 1 and "chapter" in sample.lower() and section_hits >= 2 and number_hits >= 4:
        return True
    if hint_hits >= 2 and number_hits >= 4:
        return True
    if "preface" in sample.lower() and number_hits >= 3:
        return True
    if "table of contents" in sample.lower():
        return True
    if "chapter" in sample.lower() and section_hits >= 2 and number_hits >= 4:
        return True
    return False


