        lines.append(f"### {item['category']}")
        lines.append("")
        lines.append(f"- severity: `{item['severity']}`")
        evidence = item["evidence"]
        for key, value in evidence.items():
            if isinstance(value, list):
                lines.append(f"- {key}:")
