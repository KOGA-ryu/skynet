def run_vox_study_library_smoke(shelf_root: Path) -> dict[str, Any]:
    repo_root = Path(__file__).resolve().parents[1]
    vox_root = repo_root.parent / "vox"
    study_library_path = vox_root / "app" / "study_library.py"
    if not study_library_path.exists():
        return {
            "required_for_completion": False,
            "status": "skip",
            "reason": f"missing vox study_library at {study_library_path}",
        }
    try:
        spec = importlib.util.spec_from_file_location("vox_study_library_smoke", study_library_path)
        if spec is None or spec.loader is None:
            raise ImportError(f"cannot load spec for {study_library_path}")
        module = importlib.util.module_from_spec(spec)
        sys.modules[spec.name] = module
        spec.loader.exec_module(module)
        books = module.load_study_books(root=shelf_root)
        by_id = {book.document_id: book for book in books}
        available_representative = [
            (document_id, chapter_ids)
            for document_id, chapter_ids in REPRESENTATIVE_REVIEW_BOOKS
            if document_id in by_id
        ]
        required = len(available_representative) == len(REPRESENTATIVE_REVIEW_BOOKS)
        if not available_representative:
            return {
                "checked_books": [],
                "repo_root": str(vox_root),
                "required_for_completion": False,
                "reason": "representative review set unavailable in current study shelf",
                "status": "skip",
            }
        checked: list[dict[str, Any]] = []
        for document_id, chapter_ids in available_representative:
            book = by_id[document_id]
            rows = module.load_reader_rows(book)
            if not rows:
                raise ValueError(f"empty reader rows for {document_id}")
            rendered_book = module.render_book_text(book, rows)
            if not rendered_book.strip():
                raise ValueError(f"empty rendered book text for {document_id}")
            checked_entry = {
                "document_id": document_id,
                "row_count": len(rows),
            }
            if chapter_ids:
                chapter_text = module.render_chapter_text(book, rows, chapter_ids[0])
                if not chapter_text.strip():
                    raise ValueError(f"empty rendered chapter text for {document_id}:{chapter_ids[0]}")
                checked_entry["chapter_id"] = chapter_ids[0]
            checked.append(checked_entry)
        return {
            "checked_books": checked,
            "repo_root": str(vox_root),
            "required_for_completion": required,
            "reason": None if required else "representative review set unavailable in current study shelf",
            "status": PASS,
        }
    except Exception as exc:
        return {
            "error": str(exc),
            "repo_root": str(vox_root),
            "required_for_completion": True,
            "status": FAIL,
        }


def collect_report_statuses(target_root: Path, summary_sha256: str) -> dict[str, dict[str, Any]]:
    statuses: dict[str, dict[str, Any]] = {}
    for filename in ("README.md", DEFAULT_FINAL_REVIEW_PACKET):
        path = target_root / filename
        if not path.exists():
            statuses[filename] = {"path": str(path), "status": "missing"}
            continue
        text = path.read_text(encoding="utf-8")
        embedded = extract_report_source_hash(text)
        statuses[filename] = {
            "path": str(path),
            "source_summary_sha256": embedded,
            "status": "current" if embedded == summary_sha256 else "stale",
        }
    for filename in LEGACY_DEFICIENCY_REPORTS:
        path = target_root / filename
        statuses[filename] = {
            "path": str(path),
            "status": "stale_conflicting" if path.exists() else "absent",
        }
    return statuses


def extract_report_source_hash(text: str) -> str | None:
    match = re.search(r"^- source_summary_sha256: `([0-9a-f]{64})`$", text, re.MULTILINE)
    if match is None:
        return None
    return match.group(1)


def archive_legacy_reports(target_root: Path) -> list[str]:
    archived: list[str] = []
    stale_paths = [target_root / filename for filename in LEGACY_DEFICIENCY_REPORTS if (target_root / filename).exists()]
    if not stale_paths:
        return archived
    stamp = datetime.now(UTC).strftime("%Y%m%dT%H%M%SZ")
    archive_root = target_root / DEFAULT_ARCHIVE_DIRNAME / stamp
    archive_root.mkdir(parents=True, exist_ok=True)
    for path in stale_paths:
        target = archive_root / path.name
        path.rename(target)
        archived.append(str(target))
    return archived


def build_priority_queue(books: list[dict[str, Any]]) -> list[dict[str, Any]]:
    flagged = [book for book in books if book["issues"]]
    flagged.sort(key=priority_sort_key)
    return [book_summary(book) for book in flagged]


def priority_sort_key(book: dict[str, Any]) -> tuple[int, int, int, int, str]:
    categories = set(book["issue_categories"])
    if READER_JUNK in categories or EMPTY_READER in categories:
        group = 0
    elif book["severe_issue_count"] > 0:
        group = 1
    else:
        group = 2
    return (
        group,
        -book["severe_issue_count"],
        -book["warning_issue_count"],
        -book["issue_count"],
        str(book["document_id"]),
    )


