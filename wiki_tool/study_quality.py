def study_quality_summary(
    db_path: Path = DEFAULT_DB,
    *,
    generated_at_utc: str | None = None,
    output_dir: Path = DEFAULT_STUDY_DIR,
    qa_output_dir: Path = DEFAULT_STUDY_QA_DIR,
    shelf: str = DEFAULT_STUDY_SHELF,
) -> dict[str, Any]:
    normalized_shelf = validate_shelf(shelf)
    shelf_root = output_dir / normalized_shelf
    books = load_audited_books(db_path=db_path, shelf_root=shelf_root, shelf=normalized_shelf)
    issue_counts = build_issue_counts(books)
    qa_root = qa_output_dir / normalized_shelf
    consumer_checks = build_consumer_checks(shelf_root)
    generated_at = generated_at_utc or utc_now()
    core = {
        "book_count": len(books),
        "books": [book_summary(book) for book in books],
        "built_count": sum(1 for book in books if book["status"] == "built"),
        "catalog_db": str(db_path),
        "completion_bar": "quality_done",
        "consumer_checks": consumer_checks,
        "flashcard_ready_count": sum(1 for book in books if book["flashcard_ready"]),
        "generated_at_utc": generated_at,
        "issue_counts": issue_counts,
        "output_dir": str(shelf_root),
        "qa_output_dir": str(qa_root.resolve()),
        "reader_ready_count": sum(1 for book in books if book["reader_ready"]),
        "shelf": normalized_shelf,
    }
    summary_sha256 = compute_summary_sha256(core)
    report_statuses = collect_report_statuses(qa_root, summary_sha256)
    completion = build_completion_status(issue_counts, consumer_checks)
    priority_queue = build_priority_queue(books)
    return {
        **core,
        "canonical_status_path": str((qa_root / "summary.json").resolve()),
        "completion_status": completion["completion_status"],
        "priority_queue": priority_queue,
        "remaining_severe_count": completion["remaining_severe_count"],
        "remaining_warning_count": completion["remaining_warning_count"],
        "report_statuses": report_statuses,
        "summary_sha256": summary_sha256,
    }


def study_quality_show(
    db_path: Path = DEFAULT_DB,
    identifier: str = "",
    *,
    output_dir: Path = DEFAULT_STUDY_DIR,
    shelf: str = DEFAULT_STUDY_SHELF,
) -> dict[str, Any]:
    normalized_shelf = validate_shelf(shelf)
    shelf_root = output_dir / normalized_shelf
    books = load_merged_books(db_path=db_path, shelf_root=shelf_root, shelf=normalized_shelf)
    entry = resolve_index_book(books, identifier)
    detail = audit_book_entry(shelf_root, entry)
    detail["catalog_db"] = str(db_path)
    detail["generated_at_utc"] = utc_now()
    detail["output_dir"] = str(shelf_root)
    detail["shelf"] = normalized_shelf
    return detail


